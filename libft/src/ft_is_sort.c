#include "libft.h"

int		ft_is_sort(int *s, int lenght, int (*f)(int, int))
{
	int i;

	i = 0;
	if (!s)
		return (1);
	while (i < lenght - 1)
	{
		if (f(s[i], s[i + 1]) > 0)
			return (0);
		i++;
	}
	return (1);
}
